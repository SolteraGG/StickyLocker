package com.dumbdogdiner.stixillocker.enums;

public enum ProtectionType {
	/**
	 * A container, represented by ContainerProtection.
	 */
	CONTAINER,
	/**
	 * A door, represented by DoorProtection.
	 */
	DOOR,
	/**
	 * A block where signs can also be attached to the block it is
	 * hanging/standing on. Represented AttachedProtection.
	 */
	ATTACHABLE;
}
